public class Hex {
	public static void main(String[] args) {
		System.out.printf("0x0 == %d%n", 0x0);
		System.out.printf("0x1 == %d%n", 0x1);
		System.out.printf("0xF == %d%n", 0xF);
		System.out.printf("0xFF == %d%n", 0xFF);
		System.out.printf("-0xFF == %d%n", -0xFF);
	}
}
